% Определение предиката для вычисления суммы элементов списка
sum_list([], 0). % Сумма пустого списка равна 0
sum_list([Head|Tail], Sum) :-
    sum_list(Tail, TailSum), % Рекурсивно вычисляем сумму оставшейся части списка
    Sum is Head + TailSum. % Сумма списка равна голове списка плюс сумма оставшейся части

?- sum_list([1, 2, 3, 4, 5], X), write(X).
